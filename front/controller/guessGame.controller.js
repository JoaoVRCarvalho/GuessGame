'use strict';

function againButton() {
    const resultHeaderField = document.getElementById("result-if-won");
    const scoreField = document.getElementById("user-score");
    const resultmessageField = document.getElementById("result-message");
    document.body.style.backgroundColor = "#222";
    document.querySelector(".number").style.width = "15rem";
    randomNumber = computerNumber();

    resultmessageField.innerHTML = "Start guessing..."
    resultHeaderField.innerHTML = "?";
    scoreField.innerHTML = 20;
}

function getGuessedNumber() {
    const guessedNumberField = document.getElementById("guessed-number");
    const generatedNumber = randomNumber; // Random number generated by the number generator model
    const resultmessageField = document.getElementById("result-message");
    const scoreField = document.getElementById("user-score");
    const highscoreField = document.getElementById("highscore");
    const resultHeaderField = document.getElementById("result-if-won");
    const guessedNumber = Number(guessedNumberField.value);
    let score = Number(scoreField.innerHTML)

    let result = resultCheck(guessedNumber, generatedNumber);

    resultmessageField.innerHTML = result

    switch (result) {
        case "You won":
            document.body.style.backgroundColor = "#60b347";
            document.querySelector(".number").style.width = "30rem";

            resultHeaderField.innerHTML = guessedNumber;
            highscoreField.innerHTML = score;
            break;
        case "Too high":
        case "Too low":
            scoreField.innerHTML --;
            break;
        case "No number":
            break;
    }
}